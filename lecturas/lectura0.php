        <p><b>Estudio de Viabilidad</p></b></br>

            <blockquote>
            Muchas veces cuando se emprende el desarrollo de un proyecto de Sistemas los recursos y el tiempo no 
            son realistas para su materialización sin tener pérdidas económicas y frustración profesional. 
            La viabilidad y el análisis de riesgos están relacionados de muchas maneras, si el riesgo del 
            proyecto es alto, la viabilidad de producir software de calidad se reduce, sin embargo se deben tomar 
            en cuenta cuatro áreas principales de interés:
            </blockquote></br>

        <p><b>Viabilidad económica</p></b></br>

        <blockquote>
        Una evaluación de los costos de desarrollo, comparados con los ingresos netos o beneficios obtenidos del 
        producto o Sistema desarrollado.
            </blockquote></br>

        <p><b>Viabilidad Técnica</p></b></br>

        <blockquote>
        Un estudio de funciones, rendimiento y restricciones que puedan afectar la realización de un sistema 
        aceptable.
        </blockquote></br>

        <p><b>Viabilidad Legal</p></b></br>

         <blockquote>
         Es determinar cualquier posibilidad de infracción, violación o responsabilidad legal en que se podría 
        incurrir al desarrollar el Sistema.
        </blockquote></br>

        <p><b>Alternativas</p></b></br>

        <blockquote>
        Una evaluación de los enfoques alternativos del desarrollo del producto o Sistema.
        </blockquote></br>

        <blockquote>
        El estudio de la viabilidad puede documentarse como un informe aparte para la alta gerencia.
        </blockquote></br>

        
