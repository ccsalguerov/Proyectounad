<h1>Objetivos del análisis</h1></br></br>


        <p><b>Identificación de Necesidades</b></p></br>

        <blockquote>
        Es el primer paso del análisis del sistema, en este proceso el Analista se reúne con el cliente y/o usuario 
        (un representante institucional, departamental o cliente particular), e identifican las metas globales, se 
        analizan las perspectivas del cliente, sus necesidades y requerimientos, sobre la planificación temporal y 
        presupuestal, líneas de mercadeo y otros puntos que puedan ayudar a la identificación y desarrollo del 
        proyecto.
        </blockquote></br>

        <p>Algunos autores suelen llamar a esta parte UML; Análisis de Requisitos ¨ y lo dividen en cinco partes:</p></br>


            <ol>
                <li>Reconocimiento del problema.</li>
                <li>Evaluación y Síntesis.</li>
                <li>Modelado.</li>
                <li>Especificación.</li>
                <li>Revisión.</li>
            </ol></br>

        <blockquote>
        Antes de su reunión con el analista, el cliente prepara un documento conceptual del proyecto, aunque es 
        recomendable que este se elabore durante la comunicación Cliente – analista, ya que de hacerlo el cliente 
        solo de todas maneras tendría que ser modificado, durante la identificación de las necesidades.
        </blockquote></br></br>

