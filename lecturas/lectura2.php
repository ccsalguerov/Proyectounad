                            <h1>Tipos de Requisitos</h1></br></br>


                                    <p><b>Requisitos funcionales:</b></p></br>

                                    <blockquote>
                                    Los requerimientos funcionales son aseveraciones de los servicios que el sistema debe proveer, como el sistema debe 
                                    reaccionar a entradas particulares y como el sistema debe comportarse bajo situaciones particulares. En algunos casos los
                                    requerimientos funcionales deben describir de manera explícita, lo que el sistema no debe hacer.
                                    </blockquote></br>

                                <p>(Addison, 2004)</p></br>


                                    <p><b>Requisitos no funcionales:</p></b>

                                        <blockquote>
                                        estos son restricciones sobre servicios y funcionalidades ofrecidas por el sistema, estas restricciones 
                                        normalmente aplican el sistema como un todo, y no deben aplicar a características particulares del sistema. 
                                        </blockquote></br>
        
                                <p>(Addison, 2004)</p></br>

                                    <p><b>Requisitos de dominio:</p></b>

                                        <blockquote></br>
                                        Estos son requerimientos que provienen del dominio de aplicación del sistema y reflejan características
                                        y restricciones de ese dominio. Estos pueden ser funcionales o no funcionales
                                        </blockquote></br></br>

