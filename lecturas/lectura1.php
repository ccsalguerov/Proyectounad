					<h1>Análisis de requisitos</h1></br>
			
			
			<blockquote>
			La ingeniería de requisitos es un proceso donde se refinan en detalle los requisitos de un sistema y el papel asignado al software. Este proceso tiene unos actores claves que son el desarrollador y el cliente las interacciones entre estos dos se denominan análisis “el cliente intenta replantear un sistema confuso, a nivel de descripción de datos, funciones y comportamientos, en detalles concretos. El desarrollador actúa como interrogador, como consultor, como persona que resuelve problemas y como negociador” (pressman, 2010)
		Aunque a primera vista parezca algo sencillo la comunicación entre ambos actores es muy densa, siempre hay ocasiones para malas interpretaciones o falta de información.
		El análisis de requisitos permite al ingeniero de software especificar características operacionales del software además establece las restricciones que debe cumplir el software.
		El análisis de requisitos del software se puede subdividir en cinco áreas de esfuerzo:
			</blockquote>
					<blockquote></br>
						<ol> 1. Reconocimiento del problema</ol>
						<ol> 2. Evaluación y síntesis</ol>
						<ol> 3. Modelado</ol>
						<ol> 4. Especificación</ol>
						<ol> 5. Revisión </ol>

					</blockquote>

						<p>(pressman, 2010)</p></br>


						<p> Todos los métodos de análisis se relacionan por un conjunto de principios operativos:</p></br>

					<blockquote></br>
						<ol> 1. Debe representarse y entenderse el dominio de la información de un problema.</ol>
						<ol> 2. Deben definirse las funciones que debe realizar el software</ol>
						<ol> 3. Debe representarse el comportamiento del software (como consecuencia de acontecimientos externos),</ol>
						<ol> 4. Deben dividirse los modelos que representan información, función y comportamiento de manera que se descubran los detalles por capas (o jerárquicamente).<ol>
						<ol> 5. El proceso de análisis debería ir desde la información esencial hasta el detalle de la implementación.</ol>

					</blockquote>

						<p>(pressman, 2010)</p> </br></br>

